class Peexl_DailyDeals_Model_System_Config_Source_Sidebars
{

    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        return array(
            array('value' => 0, 'label'=>Mage::helper('peexl_dailydeals')->__('Left')),
            array('value' => 1, 'label'=>Mage::helper('peexl_dailydeals')->__('Right')),
        );
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return array(
            0 => Mage::helper('adminhtml')->__('Left'),
            1 => Mage::helper('adminhtml')->__('Right'),
        );
    }

}
